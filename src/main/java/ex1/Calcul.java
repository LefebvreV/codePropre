public class Calcul {

	/**
	 * Méthode d'addition
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode de soustraction
	 * @param a
	 * @param b
	 * @return a-b
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
